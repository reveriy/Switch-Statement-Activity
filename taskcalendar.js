let day = prompt("Enter a day in a week:");
switch (day) {
    case "Monday":
        alert("You have 4 activities due today.");
        break;
    case "Tuesday":
        alert("Do your laundry.");
        break;
    case "Wednesday":
        alert("You have a 3 online class.");
        break;
    case "Thursday":
        alert("You have a dentist appointment.");
        break;
    case "Friday":
        alert("Go take some rest.");
        break;
    default:
        alert("Saturday or Sunday is work day.");
}
